// Shared i18n helper for edge functions — translates user-facing error and
// status messages so they are returned in the caller's language (en/es/fr)
// instead of being hardcoded in French.
//
// Resolution: the value in the requested language, then English as the fallback
// (the app default), then the raw key as a last resort. Mirrors the email
// translation helper in `_shared/email-branding.ts`.
//
// Wiring a function:
//   import { t, resolveLang } from "../_shared/i18n.ts";
//   const lang = resolveLang(body.language);           // body field for guests
//   throw new Error(t("checkout.eventEnded", lang));    // instead of a FR literal
// For authenticated flows you can also pass the user's `preferred_language`.

export type Lang = "en" | "es" | "fr";

export function resolveLang(input?: string | null): Lang {
  return input === "es" || input === "fr" || input === "en" ? input : "en";
}

