// Owner Help Center — complete content structure
// All text via i18n keys — real content in src/i18n/data.ts
//
// STRUCTURE: the guide mirrors the owner dashboard sidebar (app-shared.tsx).
// Top groups map 1:1 to the menu groups (Overview, Events, Marketing & CRM,
// Operations, Settings), each article maps to a menu page. "Getting started"
// is the intro, and the bonus groups (Daily operations, Manager, Staff, Client,
// Security) are cross-cutting reference guides that don't map to a single page.

export interface OwnerHelpSection {
  headingKey: string;
  bodyKey: string;
  type?: 'tip' | 'warning' | 'example' | 'steps';
  screenshotPlaceholder?: boolean;
  screenshotUrl?: string;
}

export interface OwnerHelpArticle {
  id: string;
  titleKey: string;
  descKey: string;
  icon: string;
  quickStart?: boolean;
  sections: OwnerHelpSection[];
  relatedArticleIds?: string[];
  actionLink?: { labelKey: string; path: string };
  keywords?: string[];
}

export interface OwnerHelpCategory {
  id: string;
  labelKey: string;
  icon: string;
  articles: OwnerHelpArticle[];
}

